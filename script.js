// Function to populate genre dropdown
function populategenreSelect() {
    var genres = Array.from(new Set(songs.map(song => song.genre)));

    var options = genres.map(function(genre) {
        return '<option value="'+genre+'">'+genre+'</option>';
    });

    genreSelect.innerHTML = '<option value="">类别</option>' + options.join('');
}

// Function to display the songs on the HTML page
function renderSongs(filteredSongs) {
    var html = '';
    filteredSongs.forEach(function (song) {
        html += '<tr><td>' + song.name + '</td><td>' + song.artist + '</td><td>' + song.genre + '</td></tr>';
    });
    songDetails.innerHTML = html;
}

// Function to filter songs based on the search input and the selected genre
function filterSongs() {
    var searchInputValue = searchInput.value.toLowerCase();
    var genreSelectValue = genreSelect.value;
    var filteredSongs = songs.filter(function(song) {
        var hasMatchingname = searchInputValue ?
            // If the user has typed something in the search field then filter by song name or artist name, else render all songs
            song.name.toLowerCase().includes(searchInputValue) || song.artist.toLowerCase().includes(searchInputValue) :
            true;
        var hasMatchinggenre = genreSelectValue ?
            // If the user has selected a genre then filter by genre, else render all songs
            song.genre === genreSelectValue :
            true;
        return hasMatchingname && hasMatchinggenre;
    });
    renderSongs(filteredSongs);
}

// React to changes in the search field and the dropdown select field
searchInput.addEventListener('input', filterSongs);
genreSelect.addEventListener('change', filterSongs);

